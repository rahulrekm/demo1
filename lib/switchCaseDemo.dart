import 'dart:io';

void main(){

  var day = 'wednesday';

  print("enter the day");
  
  switch(day){

    case 'monday' :
      print('day is monday');
      break;
    case 'tuesday' :
      print('day is tuesday');
      break;
    case 'wednesday' :
      print('day is wednesday');
      break;
    case 'thursday' :
      print('day is thursday');
      break;
    case 'friday' :
      print('day is friday');
      break;
    case 'saturday' :
      print('day is saturday');
      break;
    case 'sunday' :
      print('day is sunday');
      break;
    default:
      print('none of these');

  }
}